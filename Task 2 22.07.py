a = 5  # В битовой системе: 0101
b = 6  # В битовой системе: 0110

print('Побитовое "И":', a & b)  # 0101 & 0110 = 0100 = 4
print('Побитовое "ИЛИ":', a | b)    # 0101 | 0110 = 0111 = 7

print('Сдвиг влево:', a << 2)   # 00000101 << 2 = 00010100 = 20
print('Сдвиг вправо:', a >> 2)  # 00000101 >> 2 = 00000001 = 1
